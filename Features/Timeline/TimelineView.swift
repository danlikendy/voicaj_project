// MARK: - –ú–æ–¥–µ–ª–∏ –¥–∞–Ω–Ω—ã—Ö
struct TimelineDay {
    let date: Date
    let progress: Double
    let mood: MoodType
    let tags: [String]
    let keyTasks: [TimelineTask]
    let totalTasks: Int
    let completedTasks: Int
}

struct TimelineTask {
    let title: String
    let status: TimelineTaskStatus
    let color: Color
}

enum MoodType {
    case positive, neutral, stressed
    
    var emoji: String {
        switch self {
        case .positive: return "üòä"
        case .neutral: return "üòê"
        case .stressed: return "üò∞"
        }
    }
    
    var color: Color {
        switch self {
        case .positive: return .mossGreen
        case .neutral: return .warmGrey
        case .stressed: return .terracotta
        }
    }
}

enum TimelineTaskStatus {
    case completed, inProgress, planned, overdue
}

enum TimelineFilter: CaseIterable {
    case completed, inProgress, overdue
    
    var title: String {
        switch self {
        case .completed: return "–≤—ã–ø–æ–ª–Ω–µ–Ω–æ"
        case .inProgress: return "–≤ —Ä–∞–±–æ—Ç–µ"
        case .overdue: return "–ø—Ä–æ—Å—Ä–æ—á–µ–Ω–æ"
        }
    }
}



